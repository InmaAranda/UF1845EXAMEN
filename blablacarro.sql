DROP DATABASE IF EXISTS ComparticionVehiculos;
CREATE DATABASE ComparticionVehiculos;
USE ComparticionVehiculos;

DROP TABLE IF EXISTS valoraciones;
DROP TABLE IF EXISTS reservas;
DROP TABLE IF EXISTS trayectos;
DROP TABLE IF EXISTS vehiculos;
DROP TABLE IF EXISTS conductores;
DROP TABLE IF EXISTS clientes;

CREATE TABLE clientes (
    dni VARCHAR(9) PRIMARY KEY,
    nombre VARCHAR(30),
    apellido1 varchar(30),
    apellido2 varchar(30),
    ciudad VARCHAR(100),
    fecha_alta DATE
);

CREATE TABLE conductores (
    dni VARCHAR(9) PRIMARY KEY,
    nombre VARCHAR(30),
    apellido1 varchar(30),
    apellido2 varchar(30),
    ciudad VARCHAR(100),
    fecha_alta DATE
);

CREATE TABLE vehiculos (
    id INT AUTO_INCREMENT PRIMARY KEY,
    matricula VARCHAR(10) UNIQUE NOT NULL,
    marca VARCHAR(50),
    modelo VARCHAR(50),
    num_plazas INT,
    dni_conductor VARCHAR(9),
    FOREIGN KEY (dni_conductor) REFERENCES conductores(dni)
);

CREATE TABLE trayectos (
    id INT AUTO_INCREMENT PRIMARY KEY,
    origen VARCHAR(100),
    destino VARCHAR(100),
    fecha DATE,
    hora TIME,
    precio DECIMAL(5,2),
    dni_conductor VARCHAR(9),
    vehiculo_id INT,
    FOREIGN KEY (dni_conductor) REFERENCES conductores(dni),
    FOREIGN KEY (vehiculo_id) REFERENCES vehiculos(id)
);

CREATE TABLE reservas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    dni_cliente VARCHAR(9),
    trayecto_id INT,
    fecha_reserva DATE,
    FOREIGN KEY (dni_cliente) REFERENCES clientes(dni),
    FOREIGN KEY (trayecto_id) REFERENCES trayectos(id)
);

CREATE TABLE valoraciones (
    id INT AUTO_INCREMENT PRIMARY KEY,
    dni_cliente VARCHAR(9),
    dni_conductor VARCHAR(9),
    puntuacion INT CHECK (puntuacion BETWEEN 1 AND 5),
    opinion TEXT,
    fecha_valoracion DATE,
    FOREIGN KEY (dni_cliente) REFERENCES clientes(dni),
    FOREIGN KEY (dni_conductor) REFERENCES conductores(dni)
);

